-- 1. 创作类型模板表 (运营可配)
CREATE TABLE IF NOT EXISTS creative_template (
    id               INT AUTO_INCREMENT PRIMARY KEY,
    type_code        VARCHAR(20)  NOT NULL UNIQUE COMMENT '类型编码: novel/drama/podcast/radio/lecture/ad/picture_book/news',
    type_name        VARCHAR(50)  NOT NULL COMMENT '类型名称',
    icon             VARCHAR(10)  DEFAULT '📝' COMMENT '图标 emoji',
    description      VARCHAR(200) DEFAULT '' COMMENT '类型描述',
    ai_role          TEXT         NOT NULL COMMENT 'AI 角色 System Prompt',
    output_format    VARCHAR(20)  DEFAULT 'text' COMMENT '输出格式: text/dialogue/script',
    default_voices   JSON         DEFAULT NULL COMMENT '推荐音色配置 JSON',
    settings_schema  JSON         DEFAULT NULL COMMENT '该类型支持的设置项 (角色数/风格/时长等)',
    sort_order       INT          DEFAULT 0,
    enabled          TINYINT      DEFAULT 1,
    created_at       DATETIME     DEFAULT CURRENT_TIMESTAMP,
    updated_at       DATETIME     DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='创作类型模板 (运营可配)';

-- 2. 创作项目表
CREATE TABLE IF NOT EXISTS studio_project (
    id               BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id          BIGINT       NOT NULL COMMENT '用户ID',
    template_id      INT          NOT NULL COMMENT '关联 creative_template.id',
    type_code        VARCHAR(20)  NOT NULL COMMENT '冗余: 类型编码',
    title            VARCHAR(200) DEFAULT '' COMMENT '项目标题',
    inspiration      TEXT         DEFAULT NULL COMMENT '用户输入的灵感/大纲',
    outline          TEXT         DEFAULT NULL COMMENT 'AI 生成的大纲 (JSON)',
    characters       JSON         DEFAULT NULL COMMENT '角色设定 (JSON数组: [{name,desc,voiceId}])',
    status           VARCHAR(20)  DEFAULT 'draft' COMMENT 'draft/creating/editing/completed',
    total_sections   INT          DEFAULT 0 COMMENT '总段/章/幕数',
    audio_url        VARCHAR(500) DEFAULT NULL COMMENT '最终合成音频URL',
    audio_duration   INT          DEFAULT NULL COMMENT '音频时长(秒)',
    created_at       DATETIME     DEFAULT CURRENT_TIMESTAMP,
    updated_at       DATETIME     DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted          TINYINT      DEFAULT 0,
    INDEX idx_user_id (user_id),
    INDEX idx_type_code (type_code),
    INDEX idx_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='创作项目';

-- 3. 创作内容分段表 (章节/幕/段落)
CREATE TABLE IF NOT EXISTS studio_section (
    id               BIGINT AUTO_INCREMENT PRIMARY KEY,
    project_id       BIGINT       NOT NULL COMMENT '关联 studio_project.id',
    section_index    INT          NOT NULL DEFAULT 0 COMMENT '段序号 (0-based)',
    title            VARCHAR(200) DEFAULT '' COMMENT '段标题 (如: 第一章/第一幕)',
    content          TEXT         DEFAULT NULL COMMENT '文本内容',
    character_name   VARCHAR(50)  DEFAULT NULL COMMENT '角色名 (多角色时)',
    voice_id         VARCHAR(50)  DEFAULT NULL COMMENT '绑定的音色ID',
    emotion_tag      VARCHAR(50)  DEFAULT NULL COMMENT '情感标签 (happy/sad/angry...)',
    audio_url        VARCHAR(500) DEFAULT NULL COMMENT '该段音频URL',
    status           VARCHAR(20)  DEFAULT 'pending' COMMENT 'pending/generated/synthesized',
    created_at       DATETIME     DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_project_id (project_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='创作内容分段';

